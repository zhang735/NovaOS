#ifndef DICTATOR_IDT_H
#define DICTATOR_IDT_H

#include <kernel/types.h>

// -------------------------------------------------------------------------
// IDT 条目结构 (8 字节)
// -------------------------------------------------------------------------
struct idt_entry {
    uint16_t base_low;  // 中断处理函数地址的低 16 位
    uint16_t sel;       // 内核代码段选择子 (通常是 0x08)
    uint8_t  always0;   // 必须始终为 0
    uint8_t  flags;     // 标志位 (P, DPL, DT, Type)
    uint16_t base_high; // 中断处理函数地址的高 16 位
} __attribute__((packed));

// -------------------------------------------------------------------------
// IDT 指针结构 (LIDT 指令加载的对象)
// -------------------------------------------------------------------------
struct idt_ptr {
    uint16_t limit;     // IDT 表的总大小 - 1
    uint32_t base;      // IDT 表的起始线性地址
} __attribute__((packed));

// -------------------------------------------------------------------------
// 常用中断号定义
// -------------------------------------------------------------------------
#define IRQ0 32  // 时钟中断 (PIT)
#define IRQ1 33  // 键盘中断

// -------------------------------------------------------------------------
// 中断门标志位 (Flags)
// 0x8E = 1 00 0 1110 (Present, Ring 0, Interrupt Gate)
// -------------------------------------------------------------------------
#define IDT_GATE_INTERRUPT 0x8E

// -------------------------------------------------------------------------
// 函数声明
// -------------------------------------------------------------------------

/**
 * 初始化 IDT 和 PIC (中断控制器)
 */
void idt_init(void);
void pic_init(void);

/**
 * 设置一个 IDT 门
 * @param num   中断号 (0-255)
 * @param base  处理函数的地址
 * @param sel   代码段选择子
 * @param flags 权限和类型标志
 */
void idt_set_gate(uint8_t num, uint32_t base, uint16_t sel, uint8_t flags);

#endif